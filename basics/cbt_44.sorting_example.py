# Sort é usado para ordenar uma lista por Letra, numeros, maiuscula depois mínuscula
list_of_routers = ["R1","r0","R10","r100","R2","R20","r1","R0","R10","R100","R2","R20"]
list_of_routers.sort()
print(list_of_routers)
list_of_words = ["a","A", "AA", "BB", "bb", "cB", "BC", "DC", "dc", "1", "21", "3"]
list_of_words.sort()
print(list_of_words)
list_of_words.sort(reverse=True)
print(list_of_words)